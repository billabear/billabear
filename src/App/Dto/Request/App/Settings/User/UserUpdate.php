namespace App\Dto\Request\App\Settings\User;

use Symfony\Component\Validator\Constraints as Assert;

class UserUpdate
{
    #[Assert\NotBlank]
    private $roles = [];

    #[Assert\NotBlank]
    #[Assert\Email]
    private $email;

    public function getRoles(): array
    {
        return $this->roles;
    }

    public function setRoles(array $roles): void
    {
        $this->roles = $roles;
    }

    public function getEmail()
    {
        return $this->email;
    }

    public function setEmail($email): void
    {
        $this->email = $email;
    }
}
